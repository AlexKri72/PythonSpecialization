class Rectangle:
    def __init__(self, a=None, b=None):
        self.a = a
        self.b = b if b else a

    def perimeter(self):
        return (self.a + self.b) * 2

    def square(self):
        return self.a * self.b


if __name__ == '__main__':
    r1 = Rectangle(2, 3)
    r2 = Rectangle(2)
    print(f'{r1.perimeter()= } , {r1.square()= }')
    print(f'{r2.perimeter()= } , {r2.square()= }')
